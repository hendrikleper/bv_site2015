					<div class="panel" id="inspiration">
						<p>
						The main concept of 'Modus Motus' is inspired by Steve Reich's 'Pendulum Music', performed for the first time in the summer of 1968. His composition, created for a number microphones suspended in pendulous motion over corresponding loudspeakers, produced a feedback prescribed by their specific motion. An important aspect is that, determined by the very nature of the work, each performance became a unique experience.</p> 
						<p>In Reich's footsteps we explore the idiosyncrasies of the performance space and the equipment used in order to come to a distinctive encounter with various of our senses. Many of our previous installations have examined the possibilities of self-made 'interfaces' to spark, nurture and manifest human relations, musical expression and spatial experiences.</p> 
						<p>'Pendulum Music' was steered by a selection of performers and ended at the moment all microphones came to a halt above the speakers, leaving the space filled with a continuous tone. 'Modus Motus' chooses to eliminate the auditorium context and invites every spectator to take part in the performance, enabling young and old to contribute to the 'composition'. The performer-audience correlation fades away.</p>
					</div><!-- close panel -->	
						
					<div class="panel" id="concept">
						<p>It is not a coincidence that almost each playground contains a swing and pendulums are often part of rides found at amusement parks. Our object is to blur social boundaries and we often look for new dynamic ways of expression by focussing on installations with an explicit physical and playful character. We believe that the energy between 'user and system' is more than just a pure technical bond and we search for methods to express and initiate social-artistic experiences.</p>
						<p>A basic pendulum is an object that is attached to a pivot point so that it can swing freely. When given an initial push, under the influence of gravity, the object will swing back and forth over its central point establishing a harmonic motion. After analyzing this motion we realized that its trail shows several similarities to that of sound waves. In 'Modus Motus' we trace the speed and position of this basic movement and transform it into a sound and light output that exposes a more complex relation between the various components.</p> 
						<p>Different pendulums are spread throughout the space and all of them include a speaker and a light source. The combination of their individual and collective pendent motion, the generated sound and the changing light, engenders a transformation of the space. As soon as you walk through the door you will find yourself within in the piece itself and 'Modus Motus' literally absorbs the visitor. Each of the hanging objects can be set in motion separately and one can explore all its aspects individually. Yet, each visitor becomes part of a creative and collective construction generated by a 'soundless' communication, making 'Modus Motus' a catalyst for social openness and artistic expression.
</p>					
